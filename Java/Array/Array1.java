import java.util.*;
class Array1
{
    public static void main(String args[])
    {
        int num[] = {10, 20, 30, 40};
        int len;
        len = num.length;
        System.out.println("All the numbers are: ");
        for (int i = 0; i < len; i++)
        {
            System.out.println(num[i]);
        }
    }
}